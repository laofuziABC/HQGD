CREATE TABLE IF NOT EXISTS CONF_SYS_DICTIONARY (
  DICT_ID INT (11) NOT NULL COMMENT '字典编号',
  EDIT_LEVEL VARCHAR (4) NOT NULL DEFAULT 'WW' COMMENT '权限级别',
  DICT_CODE VARCHAR (50) NOT NULL COMMENT '字典编码',
  DICT_NAME VARCHAR (100) NOT NULL COMMENT '字典名称',
  COMMENTS VARCHAR (200) DEFAULT NULL COMMENT '字典说明',
  ENABLE_FLAG INT (11) NOT NULL DEFAULT '1' COMMENT '启用标志(0:禁用;1:启用);默认:1启用',
  CREATOR VARCHAR (50) NOT NULL COMMENT '创建者',
  CREATE_TIME DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  UPDATER VARCHAR (50) NOT NULL COMMENT '更新者',
  UPDATE_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (DICT_ID),
  UNIQUE KEY UNIQUE_DICT_CODE (DICT_CODE),
  KEY INDEX_DICT_ID (DICT_ID)
) ENGINE = INNODB COMMENT = '系统字典(CONF_SYS_DICTIONARY)' ;

