CREATE TABLE IF NOT EXISTS CONF_SYS_MODULE (
  MODULE_ID INT (11) NOT NULL COMMENT '模块编号',
  MODULE_CODE VARCHAR (50) NOT NULL COMMENT '模块编码',
  MODULE_NAME VARCHAR (50) NOT NULL COMMENT '模块名',
  PARTENT_MODULE_ID INT (11) NOT NULL COMMENT '父模块编号',
  ENABLE_FLAG INT (11) NOT NULL DEFAULT '1' COMMENT '启用标志(0:禁用;1:启用);默认:1启用',
  CREATOR VARCHAR (50) NOT NULL COMMENT '创建者',
  CREATE_TIME DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  UPDATER VARCHAR (50) NOT NULL COMMENT '更新者',
  UPDATE_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (MODULE_ID),
  UNIQUE KEY UNIQUE_MODULE_CODE (MODULE_CODE),
  KEY INDEX_PARTENT_MODULE_ID (PARTENT_MODULE_ID)
) ENGINE = INNODB COMMENT = '系统模块(CONF_SYS_MODULE)' ;


