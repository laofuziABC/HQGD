CREATE TABLE IF NOT EXISTS CONF_SYS_PARAMETER (
  PARAM_ID INT (11) NOT NULL COMMENT '主键',
  MODULE_ID INT (11) NOT NULL COMMENT '模块编号',
  EDIT_LEVEL VARCHAR (4) NOT NULL DEFAULT 'WW' COMMENT '权限级别',
  PARAM_CODE VARCHAR (100) NOT NULL COMMENT '参数编码',
  PARAM_NAME VARCHAR (100) NOT NULL COMMENT '参数名称',
  PARAM_VALUE VARCHAR (500) NOT NULL COMMENT '参数值',
  DEBUG_VALUE VARCHAR (500) DEFAULT NULL COMMENT '调试参数值',
  PARAM_TYPE VARCHAR (20) NOT NULL COMMENT '参数值类型',
  DATA_FILTER VARCHAR (200) DEFAULT NULL COMMENT '数据过滤条件',
  COMMENTS VARCHAR (200) DEFAULT NULL COMMENT '参数说明',
  DEL_FLG INT (11) NOT NULL DEFAULT '0' COMMENT '删除标记',
  CREATOR VARCHAR (50) NOT NULL COMMENT '创建者',
  CREATE_TIME DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  UPDATER VARCHAR (50) NOT NULL COMMENT '更新者',
  UPDATE_TIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (PARAM_ID),
  UNIQUE KEY UNIQUE_PARAM_CODE (PARAM_CODE),
  KEY INDEX_MODULE_ID (MODULE_ID)
) ENGINE = INNODB COMMENT = '系统参数(CONF_SYS_PARAMETER)' ;


